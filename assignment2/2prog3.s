       AREA STRING, CODE,READONLY
	   ENTRY
	   EXPORT start
 
start  PROC
	MOV32 R0,#STRING1
	MOV32 R11,#0X20000000
	
LOOP	LDRB R1,[R0],#1
	CMP R1,#0
	BEQ SKIP
	CMP R1,#' '
	BEQ NONE
	SUBS R1,#32
NONE	STRB R1,[R11],#1
	
	B LOOP
SKIP	STRB R1,[R11],#1	
	B  .
	ENDP
		AREA MYDATA,DATA,READONLY
STRING1 DCB "madhusudhan",0
		END